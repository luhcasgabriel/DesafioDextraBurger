


class Item {

    id = "";
    name = "";
    price = 0.4;
    createdAt = Date();
    updatedAt = Date()

    constructor(id, name, price, createdAt, updatedAt) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.createdAt = createdAt;
        this.updatedAt = updatedAt;
    }






}

export { Item }